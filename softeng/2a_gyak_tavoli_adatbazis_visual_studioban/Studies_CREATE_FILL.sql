/* Create tables of the Studies database */

CREATE TABLE [dbo].[Room] (
    [RoomSK] INT      NOT NULL IDENTITY,
    [Name]   NVARCHAR (20) NOT NULL,
    PRIMARY KEY CLUSTERED ([RoomSK] ASC)
)

CREATE TABLE [dbo].[Time]
(
	[TimeID] TINYINT NOT NULL PRIMARY KEY, 
    [Name] CHAR(11) NOT NULL
)

CREATE TABLE [dbo].[Day] (
    [DayID] TINYINT    NOT NULL,
    [Name]  NCHAR (10) NOT NULL,
    PRIMARY KEY CLUSTERED ([DayID] ASC)
)

CREATE TABLE [dbo].[Employement]
(
	[EmployementID] CHAR(1) NOT NULL PRIMARY KEY, 
    [Name] NVARCHAR(20) NOT NULL
)

CREATE TABLE [dbo].[Status]
(
	[StatusID] TINYINT NOT NULL PRIMARY KEY, 
    [Name] NVARCHAR(30) NULL
)

CREATE TABLE [dbo].[Instructor]
(
	[InstructorSK] INT NOT NULL PRIMARY KEY IDENTITY, 
    [Salutation] NVARCHAR(10) NULL, 
    [Name] NVARCHAR(50) NOT NULL, 
    [StatusFK] TINYINT NULL, 
    [EmployementFK] CHAR NULL, 
    CONSTRAINT [FK_Instructor_ToStatus] FOREIGN KEY ([StatusFK]) REFERENCES [Status]([StatusID]), 
    CONSTRAINT [FK_Instructor_ToEmployement] FOREIGN KEY ([EmployementFK]) REFERENCES [Employement]([EmployementID])
)

CREATE TABLE [dbo].[Course]
(
	[CourseSK] INT NOT NULL PRIMARY KEY IDENTITY, 
    [Name] NVARCHAR(50) NOT NULL, 
    [Code] NCHAR(10) NULL
)

CREATE TABLE [dbo].[Lesson]
(
	[LessonSK] INT NOT NULL PRIMARY KEY IDENTITY, 
    [CourseFK] INT NULL, 
    [InstructorFK] INT NULL, 
    [DayFK] TINYINT NULL, 
    [TimeFK] TINYINT NULL, 
    [RoomFK] INT NULL, 
    CONSTRAINT [FK_Lesson_ToCourse] FOREIGN KEY ([CourseFK]) REFERENCES [Course]([CourseSK]),
	CONSTRAINT [FK_Lesson_ToInstructor] FOREIGN KEY ([InstructorFK]) REFERENCES [Instructor]([InstructorSK]),
	CONSTRAINT [FK_Lesson_ToDay] FOREIGN KEY ([DayFK]) REFERENCES [Day]([DayID]),
	CONSTRAINT [FK_Lesson_ToTime] FOREIGN KEY ([TimeFK]) REFERENCES [Time]([TimeID]),
	CONSTRAINT [FK_Lesson_ToRoom] FOREIGN KEY ([RoomFK]) REFERENCES [Room]([RoomSK])
)

